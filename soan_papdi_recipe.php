                                    <li>To make Soan Papdi, sift gram flour and all-purpose flour together in a large bowl.</li>
                                    <li>Heat ghee in a heavy saucepan over moderate flame. Add the flour mixture and  <br>roast on low flame until light golden. Let it cool, stirring occasionally.</li>
                                    <li>Prepare a syrup with sugar, water, and milk, bringing it to 2 1/2 thread consistency. <br> Pour the syrup into the flour mixture and beat with a large fork until threadlike flakes form.</li>
                                    <li>Pour onto a greased surface and roll to 1-inch thickness. Sprinkle cardamom and <br>press lightly. Cool, cut into 1-inch squares, and wrap in plastic. Store in an airtight container.</li>
